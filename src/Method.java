public class Method {

    boolean isEven (int number){
        boolean result = number % 2 == 0;
        return result;
}
    boolean isOdd (int number){
        boolean result = number % 2 != 0;
        return result;
    }
    double circleField (double r){
        return 3.1415 * r * r;
    }

    int power (int number){
        return  number * number;
    }

}

